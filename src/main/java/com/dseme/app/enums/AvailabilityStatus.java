package com.dseme.app.enums;

/**
 * Enum for facilitator availability status.
 */
public enum AvailabilityStatus {
    /**
     * Facilitator is active and available for assignments.
     */
    ACTIVE,
    
    /**
     * Facilitator is on leave (temporary unavailability).
     */
    ON_LEAVE,
    
    /**
     * Facilitator is inactive (not available for assignments).
     */
    INACTIVE
}
